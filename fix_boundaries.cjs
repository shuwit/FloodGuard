const fs = require('fs');

const file = 'src/assets/data/marikina.json';
const data = JSON.parse(fs.readFileSync(file, 'utf8'));

// Remove existing Fortune and Tumana (added from Nominatim - they may be imprecise)
data.features = data.features.filter(f => !['Fortune', 'Tumana'].includes(f.properties.NAME_3));

// Based on the reference map layout and neighboring barangay coordinates:
// Fortune is EAST of Parang, south of Nangka eastern part
// Tumana is NORTH of Malanday/Jesus de la Peña, west of Concepcion Uno

function makeFeature(name, id, coords) {
  return {
    type: "Feature",
    properties: {
      ID_0: 177, ISO: "PHL", NAME_0: "Philippines",
      ID_1: 47, NAME_1: "Metropolitan Manila",
      ID_2: 956, NAME_2: "Marikina",
      ID_3: id, NAME_3: name,
      NL_NAME_3: null, VARNAME_3: null,
      TYPE_3: "Barangay", ENGTYPE_3: "Village",
      PROVINCE: "Metropolitan Manila", REGION: "Metropolitan Manila"
    },
    geometry: { type: "Polygon", coordinates: [coords] }
  };
}

